# ELEFANTES

# ENTRADA
n_elefantes = int(input())


# PROCESAMIENTO
acumulador = 2
print("1 elefante se balanceaba sobre la tela de una araña,")
print("como veia que resistia fue a llamar a otro elefante.")

while acumulador <= n_elefantes:
    print(acumulador, "elefantes se balanceaban sobre la tela de una araña,")
    # SALIDA
    print("como veia que resistia fueron a llamar a otro elefante.")
    acumulador = acumulador + 1
